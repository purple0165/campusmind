"""创建测试用的校园文档"""
from pathlib import Path
from docx import Document

def create_campus_doc():
    doc = Document()

    doc.add_heading('阳光大学学生手册', level=0)

    doc.add_heading('第一章 学校概况', level=1)
    doc.add_paragraph(
        '阳光大学创建于1958年，是一所综合性全国重点大学。学校占地面积3200亩，'
        '设有20个学院，86个本科专业，在校生28000余人，教职工2100余人。'
        '学校秉承"明德、博学、求是、创新"的校训，致力于培养高素质人才。'
    )

    doc.add_heading('第二章 校园生活指南', level=1)

    doc.add_heading('2.1 图书馆使用', level=2)
    doc.add_paragraph(
        '图书馆开放时间：周一至周五 7:30-22:30，周末 8:30-21:30。'
        '借阅规则：本科生每次最多借阅10本，借期30天，可续借一次（15天）。'
        '逾期罚款：每天每本0.2元。图书馆共有藏书280万册，电子图书150万册，'
        '设有自习座位2000个，研讨室20间可通过预约系统免费使用。'
    )

    doc.add_heading('2.2 食堂就餐', level=2)
    doc.add_paragraph(
        '学校设有三个食堂：东区食堂、西区食堂、北区食堂。'
        '营业时间：早餐 6:30-9:00，午餐 11:00-13:30，晚餐 17:00-19:30。'
        '消费方式：校园卡刷卡或微信扫码支付。'
        '人均消费：早餐 4-8元，午餐 8-15元，晚餐 8-15元。'
        '特色窗口：西区食堂二楼的麻辣香锅和北区食堂的兰州拉面深受学生欢迎。'
    )

    doc.add_heading('2.3 宿舍管理', level=2)
    doc.add_paragraph(
        '宿舍分配：按学院和年级统一安排，一般为4人间或6人间。'
        '门禁时间：周日至周四 23:00，周五周六 23:30。'
        '水电费：每月免费配额（电8度/人，水3吨/人），超出部分按居民价格收费。'
        '网络：每间宿舍提供100M校园网接口，月费20元/人，WiFi覆盖全楼。'
        '禁止使用大功率电器（超过800W），违者按校规处理。'
    )

    doc.add_heading('第三章 教务管理', level=1)

    doc.add_heading('3.1 选课制度', level=2)
    doc.add_paragraph(
        '选课时间：每学期开学前两周进行选课。'
        '选课规则：每学期修读学分不低于15学分，不超过30学分。'
        '课程类型：必修课、专业选修课、公共选修课。'
        '退改选：开学第一周可退改选，第二周仅可退选。'
        'GPA计算：采用4.0制，A=4.0, B+=3.7, B=3.3, C+=3.0, C=2.5, D=1.5, F=0。'
    )

    doc.add_heading('3.2 考试规定', level=2)
    doc.add_paragraph(
        '期末考试：安排在每学期最后两周，具体时间和地点由教务处统一公布。'
        '补考：开学第一周进行，成绩按及格（60分）或不及格记录。'
        '缓考：因特殊情况无法参加考试，需提前3个工作日向学院申请。'
        '违纪处理：作弊者该课程记0分，取消学位申请资格，并给予记过处分。'
    )

    doc.add_heading('第四章 校园安全', level=1)
    doc.add_paragraph(
        '校园安保：24小时巡逻，报警电话 0571-88889999。'
        '消防设施：每栋楼配有灭火器和消防栓，禁止占用消防通道。'
        '车辆管理：电动车需登记上牌，限速15km/h，禁止在室内充电。'
        '紧急疏散：发生紧急情况时按疏散指示牌有序撤离，集合点在操场。'
    )

    doc.add_heading('第五章 学生服务', level=1)

    doc.add_heading('5.1 心理咨询', level=2)
    doc.add_paragraph(
        '心理咨询中心位于学生活动中心3楼，提供免费心理咨询服务。'
        '预约方式：现场预约或拨打 0571-88887777。'
        '服务时间：周一至周五 9:00-17:00。'
        '保密原则：所有咨询内容严格保密，除非涉及自伤或伤人风险。'
    )

    doc.add_heading('5.2 就业指导', level=2)
    doc.add_paragraph(
        '就业指导中心位于行政楼1楼，提供职业规划、简历修改、面试辅导等服务。'
        '校园招聘：每年春秋两季举办大型招聘会，全年不定期企业宣讲会。'
        '就业率：2024届毕业生就业率达96.5%，其中升学率28%。'
        '实习推荐：与300余家企业建立合作关系，提供实习岗位。'
    )

    doc.add_heading('5.3 奖助学金', level=2)
    doc.add_paragraph(
        '国家奖学金：8000元/年，需年级前5%，无挂科记录。'
        '国家励志奖学金：5000元/年，面向家庭经济困难学生。'
        '校级奖学金：一等3000元，二等2000元，三等1000元。'
        '助学金：一等4000元，二等3000元，三等2000元，按家庭经济情况评定。'
        '勤工助学：提供图书馆、食堂、实验室等岗位，时薪15-20元。'
    )

    output_dir = Path(__file__).resolve().parent / "data"
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path = output_dir / "阳光大学学生手册.docx"
    doc.save(str(output_path))
    print(f"文档已创建: {output_path}")
    print(f"文件大小: {output_path.stat().st_size} bytes")
    return output_path

if __name__ == "__main__":
    create_campus_doc()